echo "Enter a Single Digit Number"
read number

if [ $number = 0 ]
then
	echo "ZERO"

elif [ $number = 1 ]
then
	echo "ONE"

elif [ $number = 2 ]
then
	echo "Two"

elif [ $number = 3 ]
then
	echo "THREE"

elif [ $number = 4 ]
then
	echo "FOUR"

elif [ $number = 5 ]
then
	echo "FIVE"

elif [ $number = 6 ]
then
	echo "SIX"

elif [ $number = 7 ]
then
	echo "SEVEN"

elif [ $number = 8 ]
then
	echo "EIGHT"

elif [ $number = 9 ]
then
	echo "NINE"

else
   echo "Its not a single Digit number"

fi
